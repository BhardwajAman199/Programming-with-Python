# Examples of Relational Operators 
a = 13
b = 33


print(a > b)         #  False 
print('x > y  is',a>b)
print(a < b)         # True 
print('x < y  is',a<b)
print(a == b)        #  False 
print('x == y is',a==b)
print(a != b)        #  True
print('x != y is',a!=b)
print(a >= b)        #  False 
print('x >= y is',a>=b)
print(a <= b)        #  True 
print('x <= y is',a<=b)

