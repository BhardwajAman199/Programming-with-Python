#Examples of Arithmetic Operator 
a = 9 
b = 4 
add = a + b 
print (add) 
print ('x + y =', a + b) 

sub = a - b 
print (sub) 
print ('x - y =', a - b) 
 
mul = a * b 
print (mul) 
print ('x * y =', a * b) 
 
div1 = a / b
#Division(float) of number
print (div1) 
print ('x / y =', a / b) 
 
div2 = a// b    # Division(floor) of number 
print ('x // y =', a//b)
print (div2) 
 
mod = a % b #Modulo of both number 
print(mod) 

 
 
 
 
